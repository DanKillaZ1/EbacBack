package Exemplos.ExemploMod10.forwhile;

public class ExemploPPT2 {
    public static void main(String[] args) {
        for (int contador = 1; contador <= 1000; contador++) {
            System.out.println("Valor de contador : " + contador);
            if (contador == 10) 
                break;
            
        }
    }
    
}